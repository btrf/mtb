function detectLang() {
  try {
    const saved = localStorage.getItem('mtb_lang');
    if (saved && LOCALES[saved]) return saved;
  } catch {}
  return 'ru';
}

let currentLang = detectLang();

function t(key) {
  const lang = LOCALES[currentLang];
  if (lang && lang[key] !== undefined) return lang[key];
  if (LOCALES.ru[key] !== undefined) return LOCALES.ru[key];
  return key;
}

function setLang(code) {
  currentLang = code;
  try { localStorage.setItem('mtb_lang', code); } catch {}
  renderAll();
}

function getLangCode() { return currentLang; }
